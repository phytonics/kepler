# switch branch to upload-files
git switch upload-files

# check status of upload-files
git status

# add files in references
git add references

# commit with a message
git commit -m "Upload References"

# push to upload-files
git push origin upload-files

# check the difference between branches
git diff upload-files..main

# merge git branches
git merge main upload-files

# new git status
git status

# switch back to main branch
git switch main

# check git status in main branch
git status
